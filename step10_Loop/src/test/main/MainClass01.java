package test.main;

public class MainClass01 {
	public static void main(String[] args) {
		// 콘솔창에 "안녕하세요!" 를 10번 출력해 보세요.
		for(int i=0; i<10; i++) {
			System.out.println("안녕하세요");
		}
		//콘솔창에 0 부터 9까지 출력해 보세요.
		for(int i=0; i<10; i++) {
			System.out.println(i);
		}
		//콘솔창에 1부터 10까지 출력해 보세요.
		for(int i=0; i<10; i++) {
			System.out.println(i+1);
		}
	}
}